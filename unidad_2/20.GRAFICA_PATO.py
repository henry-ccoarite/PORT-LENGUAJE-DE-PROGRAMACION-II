import sys
from OpenGL.GL import *
from OpenGL.GLUT import *
from OpenGL.GLU import *

# ----------------------------------------------------------
# ðŸ”¹ INICIALIZACIÃ“N
# ----------------------------------------------------------
def inicializar():
    glClearColor(0.1, 0.1, 0.1, 1.0)  # Fondo oscuro
    glMatrixMode(GL_PROJECTION)
    glLoadIdentity()
    glOrtho(-15.0, 15.0, -15.0, 15.0, -1.0, 1.0)

# ----------------------------------------------------------
# ðŸ”¹ FUNCIÃ“N AUXILIAR
# ----------------------------------------------------------
def lineas(coords):
    """Dibuja pares de coordenadas como lÃ­neas independientes"""
    glBegin(GL_LINES)
    for (x, y) in coords:
        glVertex2f(x, y)
    glEnd()

# ----------------------------------------------------------
# ðŸ”¹ PARTES DEL DIBUJO (solo coordenadas)
# ----------------------------------------------------------
